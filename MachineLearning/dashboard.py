    "import streamlit as st\n",
    "import pandas as pd\n",
    "import numpy as np\n",
    "from pymongo import MongoClient\n",
    "from sklearn.ensemble import RandomForestClassifier, RandomForestRegressor\n",
    "from sklearn.preprocessing import LabelEncoder\n",
    "\n",
    "# --- CONFIGURACIÓN DE PÁGINA ---\n",
    "st.set_page_config(page_title=\"Predicciones Mundial 2026\", page_icon=\"⚽\", layout=\"wide\")\n",
    "\n",
    "# --- CARGA DE DATOS Y ENTRENAMIENTO (Todo en uno para que funcione) ---\n",
    "@st.cache_resource # Esto hace que solo cargue una vez y vaya rápido\n",
    "def cargar_y_entrenar():\n",
    "    client = MongoClient(\"mongodb://localhost:27017/\")\n",
    "    db = client[\"worldCup\"]\n",
    "    coll = db[\"matches\"]\n",
    "    datos = list(coll.find({}, {\"_id\": 0}))\n",
    "    \n",
    "    for d in datos:\n",
    "        win_val = str(d.get(\"winner\", \"Draw\"))\n",
    "        d[\"winner_clean\"] = win_val.split()[-1] if \" \" in win_val else win_val\n",
    "\n",
    "    df = pd.DataFrame(datos)\n",
    "    \n",
    "    le_team = LabelEncoder()\n",
    "    le_winner = LabelEncoder()\n",
    "    todos_los_equipos = pd.concat([df['home_team'], df['away_team']]).unique()\n",
    "    le_team.fit(todos_los_equipos)\n",
    "    \n",
    "    df['home_idx'] = le_team.transform(df['home_team'])\n",
    "    df['away_idx'] = le_team.transform(df['away_team'])\n",
    "    df['target'] = le_winner.fit_transform(df['winner_clean'])\n",
    "    \n",
    "    X = df[['home_idx', 'away_idx', 'elo_diff']].fillna(0)\n",
    "    \n",
    "    # Modelo de Ganador\n",
    "    model = RandomForestClassifier(n_estimators=100).fit(X, df['target'])\n",
    "    # Modelos de Goles\n",
    "    m_goles_h = RandomForestRegressor().fit(X, df['home_score'].fillna(0))\n",
    "    m_goles_a = RandomForestRegressor().fit(X, df['away_score'].fillna(0))\n",
    "    \n",
    "    return le_team, le_winner, model, m_goles_h, m_goles_a, df\n",
    "\n",
    "# Inicializamos\n",
    "le_team, le_winner, model, m_goles_h, m_goles_a, df = cargar_y_entrenar()\n",
    "\n",
    "# --- INTERFAZ DEL DASHBOARD ---\n",
    "st.title(\"🏆 FIFA World Cup 2026 Predictor\")\n",
