    "\n",
    "with col1:\n",
    "    st.header(\"Configuración del Partido\")\n",
    "    local = st.selectbox(\"Selecciona Equipo Local\", sorted(le_team.classes_))\n",
    "    visitante = st.selectbox(\"Selecciona Equipo Visitante\", sorted(le_team.classes_))\n",
    "    elo_diff = st.slider(\"Ventaja de Elo (Fuerza)\", -500, 500, 0)\n",
    "\n",
    "with col2:\n",
    "    st.header(\"Resultado Predicho\")\n",
    "    if st.button(\"🔮 SIMULAR ENCUENTRO\"):\n",
    "        h_idx = le_team.transform([local])[0]\n",
    "        a_idx = le_team.transform([visitante])[0]\n",
    "        input_data = pd.DataFrame([[h_idx, a_idx, elo_diff]], \n",
    "                                 columns=['home_idx', 'away_idx', 'elo_diff'])\n",
    "        \n",
    "        # Predicciones\n",
    "        g_h = m_goles_h.predict(input_data)[0]\n",
